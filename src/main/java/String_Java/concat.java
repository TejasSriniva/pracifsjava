package String_Java;

public class concat {
    public static void main(String[] args) {
        String s="mithun"+"love"+"Swetha"+"love"+"Tejas";
        System.out.println(s);
        String s1="mithun";
        String ss="pavan";
        String ss2="Dharshini";
        System.out.println(s1.concat(s1));
        System.out.println(ss.concat(ss2));
    }
}
