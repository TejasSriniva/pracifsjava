package Operators;

public class Assingment_operator {
    public static void main(String[] args) {
        int a=201;
        int b=199;
        int c=301;

        a+=7;
        b+=10;
        c+=19;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        a-=100;
        b-=21;
        c-=20;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}
